public class ifElse {
    public static void main(String[] args) {
        int a = 15;
        if (a <= 10) {
            System.out.println("a is less than 10");
        } else if (a > 10) {
            System.out.println("a is greater than or equal to 10");
        } else {
            System.out.println("Not a valid input");
        }
    }
}
