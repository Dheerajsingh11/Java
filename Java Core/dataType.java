//Program for Primitive data types
public class dataType {
    public static void main(String[] args) {
        char a = 'A';
        int b = 10;
        byte c = 4;
        short s = 56;
        double d = 4.12312312;
        float f = 4.123123f;
        boolean bool = true;
        long l = 123123;
        System.out.println(a + " " + b + " " + c + " " + s + " " + d + " " + f + " " + bool + " " + l);
    }
}
