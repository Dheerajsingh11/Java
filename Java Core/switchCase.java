
public class switchCase {

    public static void main(String[] args) {
        int num = 20;
        switch (num) {
            case 5 ->
                System.out.println("It is 5");
            case 10 ->
                System.out.println("It is 10");
            case 15 ->
                System.out.println("It is 15");
            case 20 ->
                System.out.println("It is 20");
            default ->
                System.out.println("Not present");
        }
    }
}
